    // Export data in HTML/CSV/Word/Excel/Email/PDF format
    public function exportDocument($doc, $recordset, $startRec = 1, $stopRec = 1, $exportPageType = "")
    {
        if (!$recordset || !$doc) {
            return;
        }
        if (!$doc->ExportCustom) {
            // Write header
            $doc->exportTableHeader();
            if ($doc->Horizontal) { // Horizontal format, write header
                $doc->beginExportRow();
                if ($exportPageType == "view") {
                    $doc->exportCaption($this->id);
                    $doc->exportCaption($this->doctor_id);
                    $doc->exportCaption($this->service_id);
                    $doc->exportCaption($this->description);
                    $doc->exportCaption($this->submission_date);
                } else {
                    $doc->exportCaption($this->id);
                    $doc->exportCaption($this->doctor_id);
                    $doc->exportCaption($this->service_id);
                    $doc->exportCaption($this->submission_date);
                    $doc->exportCaption($this->date_updated);
                    $doc->exportCaption($this->submitted_by_user_id);
                }
                $doc->endExportRow();
            }
        }

        // Move to first record
        $recCnt = $startRec - 1;
        $stopRec = ($stopRec > 0) ? $stopRec : PHP_INT_MAX;
        while (!$recordset->EOF && $recCnt < $stopRec) {
            $row = $recordset->fields;
            $recCnt++;
            if ($recCnt >= $startRec) {
                $rowCnt = $recCnt - $startRec + 1;

                // Page break
                if ($this->ExportPageBreakCount > 0) {
                    if ($rowCnt > 1 && ($rowCnt - 1) % $this->ExportPageBreakCount == 0) {
                        $doc->exportPageBreak();
                    }
                }
                $this->loadListRowValues($row);

                // Render row
                $this->RowType = ROWTYPE_VIEW; // Render view
                $this->resetAttributes();
                $this->renderListRow();
                if (!$doc->ExportCustom) {
                    $doc->beginExportRow($rowCnt); // Allow CSS styles if enabled
                    if ($exportPageType == "view") {
                        $doc->exportField($this->id);
                        $doc->exportField($this->doctor_id);
                        $doc->exportField($this->service_id);
                        $doc->exportField($this->description);
                        $doc->exportField($this->submission_date);
                    } else {
                        $doc->exportField($this->id);
                        $doc->exportField($this->doctor_id);
                        $doc->exportField($this->service_id);
                        $doc->exportField($this->submission_date);
                        $doc->exportField($this->date_updated);
                        $doc->exportField($this->submitted_by_user_id);
                    }
                    $doc->endExportRow($rowCnt);
                }
            }

            // Call Row Export server event
            if ($doc->ExportCustom) {
                $this->rowExport($doc, $row);
            }
            $recordset->moveNext();
        }
        if (!$doc->ExportCustom) {
            $doc->exportTableFooter();
        }
    }

